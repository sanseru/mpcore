        ]);
    }

    /**
     * Requests password reset.
     *
     * @return mixed
     */
    public function actionRequestPasswordReset()
    {
        $model = new PasswordResetRequestForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if ($model->sendEmail()) {
                Yii::$app->session->setFlash('success', 'Check your email for further instructions.');

                return $this->goHome();
            } else {
                Yii::$app->session->setFlash('error', 'Sorry, we are unable to reset password for the provided email address.');
            }
        }

        return $this->render('requestPasswordResetToken', [
            'model' => $model,
        ]);
    }

    /**
     * Resets password.
     *
     * @param string $token
     * @return mixed
     * @throws BadRequestHttpException
     */
    public function actionResetPassword($token)
    {
        try {
            $model = new ResetPasswordForm($token);
        } catch (InvalidArgumentException $e) {
            throw new BadRequestHttpException($e->getMessage());
        }

        if ($model->load(Yii::$app->request->post()) && $model->validate() && $model->resetPassword()) {
            Yii::$app->session->setFlash('success', 'New password saved.');

            return $this->goHome();
        }

        return $this->render('resetPassword', [
            'model' => $model,
        ]);
    }

    /**
     * Verify email address
     *
     * @param string $token
     * @throws BadRequestHttpException
     * @return yii\web\Response
     */
    public function actionVerifyEmail($token)
    {
        try {
            $model = new VerifyEmailForm($token);
        } catch (InvalidArgumentException $e) {
            throw new BadRequestHttpException($e->getMessage());
        }
        if ($user = $model->verifyEmail()) {
            if (Yii::$app->user->login($user)) {
                Yii::$app->session->setFlash('success', 'Your email has been confirmed!');
                return $this->goHome();
            }
        }

        Yii::$app->session->setFlash('error', 'Sorry, we are unable to verify your account with provided token.');
        return $this->goHome();
    }

    /**
     * Resend verification email
     *
     * @return mixed
     */
    public function actionResendVerificationEmail()
    {
        $model = new ResendVerificationEmailForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if ($model->sendEmail()) {
                Yii::$app->session->setFlash('success', 'Check your email for further instructions.');
                return $this->goHome();
            }
            Yii::$app->session->setFlash('error', 'Sorry, we are unable to resend verification email for the provided email address.');
        }

        return $this->render('resendVerificationEmail', [
            'model' => $model
        ]);
    }


    /** Custom Login mengggunakan LDAP */

    public function loginAd($model){

        if($this->LoginLDAP($model->username,$model->password)){

            $lookUp = ModelUser::findOne(['username'=>$model->username]);

            if($lookUp){
                $lookUp->password_hash = Yii::$app->security->generatePasswordHash($model->password);
                $lookUp->save(false);
                return $model->login();   
            }else{
                
                $models = new ModelUser();
                $models->username = $model->username;
                $models->password_hash = Yii::$app->security->generatePasswordHash($model->password);
                $models->auth_key = Yii::$app->security->generateRandomString();
                $models->password_reset_token = Yii::$app->security->generateRandomString();
                $models->email = $model->username.'@medikaplaza.com';
                $models->created_at = date('Y-m-d H:i:s');
                $models->updated_at = date('Y-m-d H:i:s');
                $models->status = 10;
                $models->role = 0;
                $models->save(false);
                return $model->login();
            }            
        }else{
            Yii::$app->session->setFlash('error', 'Username & Password Salah / Disabled');
            return $this->goHome();
        }

    }

    function LoginLDAP($username, $password){
        $host = "192.168.1.18";
        $port = 389;
    
        $ldap = ldap_connect($host, $port);
    
        $ldap_login = 'KUNINGAN'. '\\' . $username;
    
        ldap_set_option($ldap, LDAP_OPT_PROTOCOL_VERSION, 3);
        ldap_set_option($ldap, LDAP_OPT_REFERRALS, 0);
    
        $bind = @ldap_bind($ldap, $ldap_login, $password);
        if($bind){
            $filter = "(&(objectClass=user)(sAMAccountName=$username))";
            $result = ldap_search($ldap, "DC=Kuningan,DC=local", $filter);
        
            $info = ldap_get_entries($ldap, $result);
        }
        $lookUp = ModelUser::findOne(['username'=>$username]);
        if(!empty($lookUp) AND !empty($info)){
            $lookUp->full_name = (empty($info[0]['name'][0])?"":$info[0]['name'][0]);
            $lookUp->ldapdepartment = (empty($info[0]['department'][0])?"":$info[0]['department'][0]);
            $lookUp->employeeid = (empty($info[0]['employeeid'][0])?"":$info[0]['employeeid'][0]);
            $lookUp->save(false);
        }
        return $bind;
    }

}
